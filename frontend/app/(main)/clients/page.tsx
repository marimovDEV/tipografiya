"use client"

import { useState, useEffect } from "react"
import { Button } from "@/components/ui/button"
import { Card, CardContent, CardHeader, CardTitle } from "@/components/ui/card"
import { Input } from "@/components/ui/input"
import { Badge } from "@/components/ui/badge"
import { Switch } from "@/components/ui/switch"
import { Label } from "@/components/ui/label"
import { Plus, Search, Phone, Building2, User, Wallet, AlertCircle, LayoutGrid, List, MoreVertical, CreditCard } from "lucide-react"
import Link from "next/link"
import { Client } from "@/lib/types"
import { fetchWithAuth } from "@/lib/api-client"
import { Avatar, AvatarFallback, AvatarImage } from "@/components/ui/avatar"
import { Table, TableBody, TableCell, TableHead, TableHeader, TableRow } from "@/components/ui/table"
import { DropdownMenu, DropdownMenuContent, DropdownMenuItem, DropdownMenuTrigger } from "@/components/ui/dropdown-menu"
import { HelpModeToggle } from "@/components/settings/HelpModeToggle"
import { HelpText, HelpBox } from "@/components/ui/help-text"

const statusConfig = {
  new: { label: "Yangi", color: "bg-blue-500/10 text-blue-500 border-blue-500/20" },
  regular: { label: "Doimiy", color: "bg-green-500/10 text-green-500 border-green-500/20" },
  vip: { label: "VIP", color: "bg-purple-500/10 text-purple-500 border-purple-500/20" },
  blacklist: { label: "Qora Ro'yxat", color: "bg-red-500/10 text-red-500 border-red-500/20" },
}

export default function ClientsPage() {
  const [searchTerm, setSearchTerm] = useState("")
  const [showDebtors, setShowDebtors] = useState(false)
  const [viewMode, setViewMode] = useState<"grid" | "list">("grid")
  const [clients, setClients] = useState<Client[]>([])
  const [loading, setLoading] = useState(true)

  useEffect(() => {
    fetchClients()
  }, [])

  const fetchClients = async () => {
    try {
      const response = await fetchWithAuth("/api/customers/")
      if (!response.ok) throw new Error("Failed to fetch clients")
      const data = await response.json()
      setClients(data)
    } catch (error) {
      console.error("Error fetching clients:", error)
    } finally {
      setLoading(false)
    }
  }

  const filteredClients = clients.filter(
    (client) => {
      const matchesSearch = client.full_name.toLowerCase().includes(searchTerm.toLowerCase()) ||
        (client.company && client.company.toLowerCase().includes(searchTerm.toLowerCase())) ||
        (client.phone && client.phone.includes(searchTerm));

      const matchesDebtor = showDebtors ? (client.balance || 0) < 0 : true;

      return matchesSearch && matchesDebtor;
    }
  )

  const getInitials = (name: string) => {
    return name
      .split(" ")
      .map((n) => n[0])
      .join("")
      .toUpperCase()
      .slice(0, 2)
  }

  return (
    <div className="space-y-6 pb-20">
      <div className="flex flex-col sm:flex-row items-start sm:items-center justify-between gap-4">
        <div>
          <h1 className="text-3xl font-black tracking-tight font-outfit">Mijozlar</h1>
          <p className="text-muted-foreground mt-1 font-medium">Jami: {clients.length} ta hamkor</p>
        </div>
        <div className="flex gap-2">
          <HelpModeToggle />
          <Link href="/clients/new">
            <Button className="font-bold shadow-lg rounded-xl h-11">
              <Plus className="h-4 w-4 mr-2" />
              Yangi mijoz
            </Button>
          </Link>
        </div>
      </div>

      <HelpBox variant="purple" title="ðŸ‘¥ Mijozlar boshqaruvi">
        Doimiy mijozlaringizni saqlang. <strong>Qarzdorlar</strong> rejimini yoqib, qarz berganlarni ko'ring. <strong>Grid/List</strong> tugmalari bilan ko'rinishni o'zgartiring.
      </HelpBox>

      <div className="flex flex-col sm:flex-row gap-4 items-center justify-between">
        <div className="relative flex-1 w-full max-w-md">
          <Search className="absolute left-3 top-3 h-4 w-4 text-muted-foreground" />
          <Input
            placeholder="Mijoz qidirish..."
            className="pl-10 h-10 bg-card border-border rounded-xl"
            value={searchTerm}
            onChange={(e) => setSearchTerm(e.target.value)}
          />
        </div>

        <div className="flex items-center gap-4 w-full sm:w-auto justify-end">
          <div className="flex items-center gap-2 bg-card border border-border px-3 py-2 rounded-xl shadow-sm">
            <Switch id="debtors-mode" checked={showDebtors} onCheckedChange={setShowDebtors} />
            <Label htmlFor="debtors-mode" className="cursor-pointer text-xs font-bold text-muted-foreground">Qarzdorlar</Label>
          </div>

          <div className="flex items-center gap-1 bg-muted/20 p-1 rounded-lg border border-border">
            <Button
              variant={viewMode === "grid" ? "secondary" : "ghost"}
              size="icon"
              className="h-8 w-8 rounded-md"
              onClick={() => setViewMode("grid")}
            >
              <LayoutGrid className="h-4 w-4" />
            </Button>
            <Button
              variant={viewMode === "list" ? "secondary" : "ghost"}
              size="icon"
              className="h-8 w-8 rounded-md"
              onClick={() => setViewMode("list")}
            >
              <List className="h-4 w-4" />
            </Button>
          </div>
        </div>
      </div>

      {loading ? (
        <div className="flex items-center justify-center py-20">
          <div className="animate-spin rounded-full h-8 w-8 border-b-2 border-primary"></div>
        </div>
      ) : filteredClients.length === 0 ? (
        <div className="text-center py-20 text-muted-foreground">
          Mijozlar topilmadi
        </div>
      ) : viewMode === "grid" ? (
        <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 xl:grid-cols-4 gap-4">
          {filteredClients.map((client) => {
            const status = statusConfig[client.status || 'new'] || statusConfig.new;
            const balance = Number(client.balance || 0);

            return (
              <Card key={client.id} className="group hover:border-primary/50 transition-all duration-300 hover:shadow-xl bg-card border-border overflow-hidden rounded-2xl">
                <CardHeader className="p-4 pb-2 flex flex-row items-center gap-3 space-y-0">
                  <Avatar className="h-12 w-12 border-2 border-muted">
                    <AvatarFallback className="font-bold bg-primary/10 text-primary">{getInitials(client.full_name)}</AvatarFallback>
                  </Avatar>
                  <div className="flex-1 overflow-hidden">
                    <CardTitle className="text-base font-bold truncate">{client.full_name}</CardTitle>
                    {client.company && (
                      <div className="flex items-center gap-1 text-xs text-muted-foreground truncate">
                        <Building2 className="h-3 w-3" /> {client.company}
                      </div>
                    )}
                  </div>
                  <DropdownMenu>
                    <DropdownMenuTrigger asChild>
                      <Button variant="ghost" size="icon" className="h-8 w-8 -mr-2">
                        <MoreVertical className="h-4 w-4 text-muted-foreground" />
                      </Button>
                    </DropdownMenuTrigger>
                    <DropdownMenuContent align="end">
                      <Link href={`/clients/${client.id}`}><DropdownMenuItem>Ko'rish</DropdownMenuItem></Link>
                      <Link href={`/clients/${client.id}/edit`}><DropdownMenuItem>Tahrirlash</DropdownMenuItem></Link>
                    </DropdownMenuContent>
                  </DropdownMenu>
                </CardHeader>
                <CardContent className="p-4 pt-2">
                  <div className="flex gap-2 mb-4">
                    <Badge variant="outline" className={`${status.color} border font-bold h-6 rounded-md`}>
                      {status.label}
                    </Badge>
                  </div>

                  <div className="p-3 bg-muted/30 rounded-xl space-y-2">
                    <div className="flex justify-between items-center text-xs text-muted-foreground font-medium">
                      <span>Balans holati</span>
                      <Wallet className="h-3.5 w-3.5" />
                    </div>
                    <div className="flex items-baseline justify-between">
                      <span className={`text-lg font-bold font-mono ${balance < 0 ? 'text-red-500' : 'text-emerald-500'}`}>
                        {balance.toLocaleString()}
                      </span>
                      <span className="text-xs text-muted-foreground font-medium">so'm</span>
                    </div>
                    {/* Visual Balance Bar */}
                    <div className="w-full h-1.5 bg-muted rounded-full overflow-hidden flex">
                      {balance < 0 ? (
                        <div className="bg-red-500 h-full rounded-full" style={{ width: '100%' }}></div>
                      ) : (
                        <div className="bg-emerald-500 h-full rounded-full" style={{ width: '100%' }}></div>
                      )}
                    </div>
                  </div>

                  <div className="mt-4 grid grid-cols-2 gap-2">
                    <Link href={`/orders/new?client=${client.id}`} className="w-full">
                      <Button size="sm" className="w-full text-xs font-bold rounded-lg" variant="outline">
                        Buyurtma
                      </Button>
                    </Link>
                    <Button size="sm" variant="secondary" className="w-full text-xs font-bold rounded-lg">
                      <Phone className="h-3.5 w-3.5 mr-1" /> Aloqa
                    </Button>
                  </div>
                </CardContent>
              </Card>
            )
          })}
        </div>
      ) : (
        <div className="rounded-2xl border bg-card overflow-hidden">
          <Table>
            <TableHeader className="bg-muted/50">
              <TableRow className="hover:bg-transparent border-b-border">
                <TableHead className="w-[50px]"></TableHead>
                <TableHead className="font-bold">Mijoz</TableHead>
                <TableHead className="font-bold">Telefon</TableHead>
                <TableHead className="font-bold">Status</TableHead>
                <TableHead className="text-right font-bold">Balans</TableHead>
                <TableHead className="w-[50px]"></TableHead>
              </TableRow>
            </TableHeader>
            <TableBody>
              {filteredClients.map((client) => {
                const status = statusConfig[client.status || 'new'] || statusConfig.new;
                const balance = Number(client.balance || 0);
                return (
                  <TableRow key={client.id} className="hover:bg-muted/30">
                    <TableCell>
                      <Avatar className="h-8 w-8">
                        <AvatarFallback className="text-xs bg-muted text-muted-foreground">{getInitials(client.full_name)}</AvatarFallback>
                      </Avatar>
                    </TableCell>
                    <TableCell>
                      <div className="font-bold text-sm">{client.full_name}</div>
                      {client.company && <div className="text-xs text-muted-foreground">{client.company}</div>}
                    </TableCell>
                    <TableCell className="font-mono text-xs">{client.phone}</TableCell>
                    <TableCell>
                      <Badge variant="outline" className={`${status.color} border px-2 py-0 h-5`}>{status.label}</Badge>
                    </TableCell>
                    <TableCell className={`text-right font-mono font-bold ${balance < 0 ? 'text-red-500' : 'text-emerald-500'}`}>
                      {balance.toLocaleString()}
                    </TableCell>
                    <TableCell>
                      <Link href={`/clients/${client.id}`}>
                        <Button size="icon" variant="ghost" className="h-8 w-8 text-muted-foreground"><CreditCard className="h-4 w-4" /></Button>
                      </Link>
                    </TableCell>
                  </TableRow>
                )
              })}
            </TableBody>
          </Table>
        </div>
      )}
    </div>
  )
}
